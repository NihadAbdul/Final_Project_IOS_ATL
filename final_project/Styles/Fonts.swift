import UIKit

struct TextStyles{
    struct Title{
        static let large = UIFont(name: "Nunito-Bold", size: 24)
        static let medium = UIFont(name: "Nunito-Medium", size: 16)
        static let small = UIFont(name: "Nunito-Medium", size: 12)
    }
    struct Subtitle{
        static let medium = UIFont(name: "Nunito-SemiBold", size: 14)
        static let small = UIFont(name: "Nunito-Medium", size: 12)
    }
    struct Description{
        static let small = UIFont(name: "Nunito-Medium", size: 14)
    }
    struct Placeholder{
        static let medium = UIFont(name: "Nunito-Medium", size: 14)
    }
    struct Button{
        static let medium = UIFont(name: "Nunito-Medium", size: 14)
        static let large = UIFont(name: "Nunito-SemiBold", size: 16)
    }
}

